-- In SQL, there are several date and time data types that you can use to store and manipulate date and time values. Some of the common date and time data types include:

-- 1. DATE: This data type is used to store date values in the format 'YYYY-MM-DD'. It does not include time information.

-- 2. TIME: This data type is used to store time values in the format 'HH:MM:SS'. It does not include date information.

-- 3. DATETIME: This data type is used to store both date and time values in the format 'YYYY-MM-DD HH:MM:SS'.

-- 4. TIMESTAMP: This data type is used to store date and time values, but it also includes time zone information. It is often used for tracking when a record was created or last updated.

-- 5. YEAR: This data type is used to store year values in the format 'YYYY'. It is often used for storing birth years or other year-related information.

